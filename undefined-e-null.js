// o undefined é quando você cria uma varável, mas não declara. 
// O null é quando não há valores. 

let nao_definido;
console.log(nao_definido);
console.log(typeof nao_definido)

// quando eu crio uma variável mas não quero atribuir valor algum, coloco o valor (null).

let nao_atribuido = null;
console.log(nao_atribuido)
console.log(typeof nao_atribuido);