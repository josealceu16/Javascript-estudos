/*  
--> soma = (+)
--> subtração = (-)
--> multiplicação = (*)
--> divisão = (/)
--> potenciação = (**)
--> resto da divisao = (%)
*/

let n1 = 10;
let n2 = 2;
console.log(n1 + n2);
console.log(n1 - n2)
console.log(n1 * n2);
console.log(n1 / n2);
console.log(n1 ** n2);
console.log(n1 % n2); // normalmente usamos o % para saber se sobra resto ou se algum numero é multiplo de outro...



